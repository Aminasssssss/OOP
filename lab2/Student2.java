package training2;

public class Student2 {

		private  String name;
		private String id;
		private int yearOfStudy;
		
		public Student2(String name, String id) {
			this.name= name;
			this.id=id;
			this.yearOfStudy=1;
			
		}
		
		public String getName() {
			return name;
			
		}
		public String getId() {
			return id;
			
		}
		public int getYearOfStudy() {
			return yearOfStudy;
			
			
		}
		
		public void incrementYearOfStudy() {
			yearOfStudy++;
			
		}
		
		
			
		
			
		
		

		



}
